function love.conf(t)
    t.window.width = 1024           -- Set the window width
    t.window.height = 800           -- Set the window height
    t.window.title = "Smoothie Cup" -- The window title (string)
    t.window.centered = true        -- Center the window on the screen (boolean)
    --t.window.icon = "path/to/icon.png" -- Filepath to an image to use as the window's icon (string)
end
